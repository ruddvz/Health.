function getLang() {
  return localStorage.getItem("np_lang") || "en";
}

function walk(obj, path) {
  return path.split(".").reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), obj);
}

function t(key, vars) {
  const lang = getLang();
  let val = walk(STRINGS[lang], key);
  if (val === undefined) val = walk(STRINGS.en, key);
  if (typeof val !== "string") return key;
  if (vars) {
    return val.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
  }
  return val;
}

function mealName(dietKey, dayIndex, slot) {
  const lang = getLang();
  const row = MEAL_NAMES[dietKey]?.[dayIndex];
  if (!row || !row[slot]) return "";
  const cell = row[slot];
  if (!cell) return "";
  return cell[lang] || cell.en || "";
}

function dayLabel(i) {
  const lang = getLang();
  const arr = STRINGS[lang]?.days || STRINGS.en.days;
  return arr[i] || STRINGS.en.days[i];
}

export { STRINGS, MEAL_NAMES, getLang, t, mealName, dayLabel };
